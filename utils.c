#include "push_swap.h"

void	ft_error(void)
{
	ft_putendl_fd("Error", 1);
	exit(0);
}

int	is_sorted(t_list **stack)
{
	t_list	*check;

	check = *stack;
	while (check && check->next)
	{
		if (check->value > check->next->value)
			return (0);
		check = check->next;
	}
	return (1);
}

t_list	*ft_lstnew(int value)
{
	t_list	*new;

	new = (t_list *) malloc(sizeof(*new));
	if (!new)
		return (NULL);
	new->value = value;
	new->index = -1;
	new->next = NULL;
	return (new);
}

int	get_distance(t_list **stack, int index)
{
	t_list	*head;
	int		distance;

	distance = 0;
	head = *stack;
	while (head)
	{
		if (head->index == index)
			break ;
		distance++;
		head = head->next;
	}
	return (distance);
}
